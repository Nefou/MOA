vale = int(input("Введите день недели"))

if vale == 1:
    print("Это понедельник")
elif vale == 2:
    print("Это вторник")
elif vale == 3:
    print ("Это среда")
elif vale == 4:
    print ("Это четверг")
elif vale == 5:
    print ("Это пятница")
elif vale == 6:
    print ("Это субота")
elif vale == 7:
    print ("Это воскресенье")
else:
    print("Проверьте коректность ваших данных")
